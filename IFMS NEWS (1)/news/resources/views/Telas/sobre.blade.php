<h1 class="title_conteudo_pagina">História</h1>
<div class="entry_conteudo_pagina">
<p>O Instituto Federal de Educação, Ciência e Tecnologia de Mato Grosso do Sul (IFMS) é a primeira instituição pública federal a oferecer educação profissional e tecnológica no Estado. Foi criado pela Lei nº 11.892, de dezembro de 2008, quando o Ministério da Educação (MEC) reestruturou a Rede Federal de Educação Profissional, Científica e Tecnológica.</p>
<p>Com campi em dez municípios – Aquidauana, Campo Grande, Corumbá, Coxim, Dourados, Jardim, Naviraí, Nova Andradina, Ponta Porã e Três Lagoas – o IFMS oferta educação profissional e tecnológica nos níveis básico e superior em diferentes modalidades de ensino com inserção na pesquisa aplicada e em ações de extensão tecnológica.</p>
<p>O IFMS oferece cursos de nível médio, graduação, pós-graduação, educação a distância, além de qualificação profissional por meio de cursos de Formação Inicial e Continuada (FIC) e programas do Governo Federal, como Pronatec e Mulheres Mil.</p>
<p><strong>Implantação</strong></p>
<p>O processo de implantação do IFMS começou em 2007, com a sanção da Lei nº 11.534, que criou escolas técnicas e agrotécnicas federais. Na ocasião, foram instituídas a Escola Técnica Federal de Mato Grosso do Sul, com sede em Campo Grande, e a Escola Agrotécnica Federal de Nova Andradina.</p>
<p>No ano seguinte, com a reestruturação da Rede Federal, o IFMS foi criado com a previsão de instalação dos campi Campo Grande e Nova Andradina. Para que a nova instituição entrasse em funcionamento, o MEC instituiu a Universidade Tecnológica Federal do Paraná (UTFPR) como tutora do processo de implantação.</p>
<p>Em 2009, o novo projeto de expansão da Rede Federal consolidou o caráter regional de atuação do IFMS com a criação de outros cinco campi nos municípios de Aquidauana, Corumbá, Coxim, Ponta Porã e Três Lagoas.</p>
<p>O Campus Nova Andradina foi o primeiro a entrar em funcionamento, em 2010. No ano seguinte, os outros seis campi iniciaram as atividades de ensino. Em 2014, foram implantadas três novas unidades nos municípios de Dourados, Jardim e Naviraí.</p>
<p><strong>Rede Federal</strong></p>
<p>É formada por 38 Institutos Federais, dois Centros Federais de Educação Tecnológica, 24 Escolas Técnicas vinculadas a Universidades Federais, a Universidade Tecnológica Federal do Paraná (UTFPR) e o Colégio Pedro II.</p>
<p>São 562 unidades em funcionamento e mais de 1 milhão de matrículas atendidas, de acordo com o Ministério da Educação (MEC).</p>
<p>Os Institutos Federais têm natureza jurídica de autarquia. São instituições com autonomia administrativa, patrimonial, financeira, didático-pedagógica e disciplinar, sendo que a estrutura de organização e funcionamento são semelhantes.</p>
