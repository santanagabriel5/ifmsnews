<p><strong>Missão</strong></p>
<p>Promover a educação de excelência por meio do ensino, pesquisa e extensão nas diversas áreas do conhecimento técnico e tecnológico, formando profissional humanista e inovador, com vistas a induzir o desenvolvimento econômico e social local, regional e nacional.</p>
<p><strong>Visão</strong></p>
<p>Ser reconhecido como uma instituição de ensino de excelência, sendo referência em educação, ciência e tecnologia no Estado de Mato Grosso do Sul.</p>
<p><strong>Valores</strong></p>
<p>Inovação;<br />
Ética;<br />
Compromisso com o desenvolvimento local e regional;<br />
Transparência;<br />
Compromisso Social.</p>

